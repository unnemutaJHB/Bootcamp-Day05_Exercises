char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int i;
	i = 0;
	while(src[i] != '\0' && n > i)
	{
		dest[i] = src[i];
		i++;
	}	
	while(n > i)
	{
		dest[i] = '\0';
	}
	dest[i] = '\0';
	return dest;
}
