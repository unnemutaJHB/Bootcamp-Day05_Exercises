int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int u;
	u = 0;
	while(s1[u] != '\0' && s2[u] != '\0' && n >= u)
	{
		if(s1[u] != s2[u])
		{
			return (s1[u] - s2[u]);
		}
		u++;
	}
	return 0;	
}
